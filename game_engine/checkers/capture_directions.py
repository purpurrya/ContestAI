def expected_diagonals_count(cell_id: int) -> int:
    if cell_id in FIVE_DIAGONAL_CELLS:
        return 5
    if cell_id in EDGE_CELLS:
        return 2
    return 4


def build_capture_landings(
    line_pairs: Dict[int, List[Tuple[int, int]]]
) -> Dict[Tuple[int, int], List[int]]:
    out: Dict[Tuple[int, int], List[int]] = {}
    for over_id, pairs in line_pairs.items():
        for item in pairs:
            if isinstance(item, (list, tuple)) and len(item) == 2:
                a, b = item
                out.setdefault((a, over_id), []).append(b)
                out.setdefault((b, over_id), []).append(a)
    return out
